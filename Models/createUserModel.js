import mango from "mongoose";

const createUserSchema = new mango.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

export default mango.model("UserCreate", createUserSchema);
